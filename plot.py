import ROOT

file15 = ROOT.TFile("SPSRes/analysis_413221453221013042711_DUT_thr80.root")
file10 = ROOT.TFile("SPSRes/analysis_415000658221014000702_DUT_thr80.root")

hist15 = file15.Get("AnalysisDUT/APTS_3/seedChargeAssociated")
hist10 = file10.Get("AnalysisDUT/APTS_3/seedChargeAssociated")
ROOT.gStyle.SetOptTitle(0)
cv = ROOT.TCanvas("","",1600,1200)
hist15.GetYaxis().SetTitle("Entries (a.u.)")
hist15.Rebin(40)
hist10.Rebin(40)
hist15.GetXaxis().SetRangeUser(0,4000)
hist15.Scale(1./hist15.GetEntries())
hist10.Scale(1./hist10.GetEntries())
hist15.SetLineColor(ROOT.kRed)
hist15.Draw("")
hist10.Draw("SAME")
cv.SetLogy(1)
hist15.SetTitle("AF15P")
hist10.SetTitle("AF10P")
cv.BuildLegend()
cv.SaveAs("comparison.png")

